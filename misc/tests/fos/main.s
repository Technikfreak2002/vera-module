;-----------------------------------------------------------------------------
; psgtest.s
;-----------------------------------------------------------------------------

	.include "lib.inc"

	.import sdcard_init

;-----------------------------------------------------------------------------
; Variables
;-----------------------------------------------------------------------------
	.rodata

	.bss

	.zeropage


;-----------------------------------------------------------------------------
; Entry point
;-----------------------------------------------------------------------------
	.code
	.global entry
entry:
	jsr sdcard_init
	bcs ok
	lda #'0'
	jsr $FFD2
	rts

ok:	lda #'1'
	jsr $FFD2


	; Wait forever
loop:	rts

